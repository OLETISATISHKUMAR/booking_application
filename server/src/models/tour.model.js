const mongoose = require("mongoose");

const TourSchema = new mongoose.Schema(
  {
    username: {
      type: String,
      required: true,
    },
    email: {
      type: String,
      required: true,
      unique: true,
    },
    password: {
      type: String,
      required: true,
    },
  },
  {
    timestamps: true,
  }
);

const Tours = mongoose.model("Tours", TourSchema);

module.exports = Tours;
